  const storageKey = 'agrisense_lang';
  let currentLanguage = localStorage.getItem(storageKey) || 'en';

  function translatePage(lang) {
    const dict = translations[lang] || translations.en;
    document.querySelectorAll('[data-translate]').forEach(el => {
      const key = el.getAttribute('data-translate');
      if (dict[key]) {
        el.textContent = dict[key];
      }
    });
  }

  function initLanguage() {
    const select = document.getElementById('languageSelect');
    if (select) {
      select.value = currentLanguage;
      select.addEventListener('change', function () {
        currentLanguage = this.value;
        localStorage.setItem(storageKey, currentLanguage);
        translatePage(currentLanguage);
      });
    }
    translatePage(currentLanguage);
  }

  function highlightActiveNav() {
    const currentPath = window.location.pathname.split('/').pop() || 'index.html';
    const navLinks = document.querySelectorAll('nav a');

    navLinks.forEach(link => {
      const linkPath = link.getAttribute('href');
      // Simple check: if href matches current path
      if (linkPath === currentPath) {
        link.classList.add('text-green-600', 'font-bold');
        link.classList.remove('hover:text-green-600');
      } else {
        link.classList.remove('text-green-600', 'font-bold');
        link.classList.add('hover:text-green-600');
      }
    });
  }

  document.addEventListener('DOMContentLoaded', () => {
    initLanguage();
    highlightActiveNav();
  });
